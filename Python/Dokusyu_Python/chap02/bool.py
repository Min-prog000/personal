# 論理型(bool)

# 真（True）
data1 = True
# 偽（False）
data2 = False

# 0はFalse それ以外はTrue
data3 = 1
data4 = 0

# リスト・辞書は中身の有無にかかわらずFalse
data5 = [1, 2, 3]
data6 = {}

print(data1 == True)   # True
print(data2 == True)   # False
print(data3 == True)   # True
print(data4 == True)   # False
print(data5 == True)   # False
print(data6 == True)   # False