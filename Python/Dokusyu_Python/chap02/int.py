# 整数型（int）

# 10進数リテラル(0~9)
data1 = 19
data2 = -5

# 16進数リテラル(0~9, a~f)
# prefix : 0x
data3 = 0xff
data4 = 0xA3B2EE

# 8進数リテラル(0~7)
# prefix : 0o
data5 = 0o457
data6 = 0o13

# 2進数リテラル(0, 1)
# prefix : 0b
data7 = 0b1001
data8 = 0b11

# printでの出力は10進数
print(data1)    # 19
print(data2)    # -5
print(data3)    # 255
print(data4)    # 10728174
print(data5)    # 303
print(data6)    # 11
print(data7)    # 9
print(data8)    # 3